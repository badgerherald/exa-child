<p>Below are applications for open positions at The Badger Herald for Fall, 2014</p>


<hr/>
<h4>Associate News Editors</h4>
<p>The Associate News Editors on both the print and digital teams run their respective beats (city, state and campus news) to produce daily and long-form content for the News Department. An excellent opportunity to grow as a writer and editor, the associate news positions provide a platform for you to grow your portfolio and improve your leadership skills.</p>
<ul>
	<li><a href="http://badgerherald.com/wordpress/media/2014/01/online-news-associates-2014.pdf">Online Associate Application</a></li>
		<li><a href="http://badgerherald.com/wordpress/media/2014/01/print-news-associate-2014.pdf">Print Associate Application</a></li>
</ul>

<hr/>


<h4>Design Director</h4>
<p>The Design Director is responsible for continuing to innovate the design of The Badger Herald’s print product in addition to managing a group of page designers. This is an excellent creative opportunity for all interested in graphic design and/or the future of print.</p>
<ul>
	<li><a href="http://badgerherald.com/wordpress/media/2014/01/design-directors-2014.pdf">Design Director Application</a></li>
</ul>

<hr/>



<h4>Photo Editors</h4>
<p>The Photo Editors edit and help produce all photo content for both the digital and print products in addition to developing unique section content and managing a team of paid beat photographers. This position allows students to build their individual photo portfolios as well as cover some of the areas’s most exciting events.</p>
<ul>
	<li><a href="http://badgerherald.com/wordpress/media/2014/01/photo-editors-2014.pdf">Photo Editor Application</a></li>
</ul>

<hr/>


<h4>Advertising Manager</h4>
<p>As Advertising Manager you will oversee the account executive's sales and also direct their time in the office. In addition, you will work closely with the Advertising Director with client relationships, placing advertisements, and other administrative tasks. Managers are expected to uphold a strong professional demeanor and to set high standards for the account executives. This position comes with a base salary with commission.</p>
<p>
&middot; At least one year of advertising, journalism, or business experience<br/>
&middot; Excellent communication skills, a great work ethic, and an interest to work in a fast-paced environment<br/>
&middot; Able to commit time in the summer of 2014<br/>
&middot; High level of organizational skills<br/>
&middot; Desire to take on a leadership position<br/>
</p>

<ul>
	<li><a href="http://badgerherald.com/wordpress/media/2014/01/Advertising-Manager-Application.pdf">Advertising Manager Application</a></li>
</ul>

<hr/>

<h4>Advertising Executives</h4>
<p>As an advertising executives you will work closely with 40-50 local and national clients. Executives develop a relationship with their clients to meet their advertising needs and to  reach biweekly sales goals. Executives are trained to learn our media kit and develop an in depth understanding of our print and online advertising. This is a paid position.</p>
<p>
&middot; Must be available to work at least 12 hours Monday-Friday 9:00AM-5:00PM when not in class<br/>
&middot; An interest in marketing, business, and/or advertising<br/>
&middot; Clear and effective communication skills<br/>
&middot; Strong planning, organizational, and time management skills<br/>
&middot; An outgoing personality <br/>
</p>
<ul>
	<li><a href="http://badgerherald.com/wordpress/media/2014/05/Advertising-Executive-Application.pdf">Advertising Executive Application</a></li>
</ul>

<hr/>


<h4>Business Manager</h4>
<p>The Business Manager works with the Publisher and Advertising Executives to assist clients with questions regarding their accounts, assists with weekly and monthly billing, and performs numerous miscellaneous duties to ensure smooth operations within the Business Department. This salaried position requires 10-12 hours weekly.</p>
<ul>
	<li><a href="http://badgerherald.com/wordpress/media/2014/01/business-manager-2014.pdf">Business Manager Application</a></li>
</ul>

<hr/>

<h4>Brand Ambassadors</h4>
<p>Brand Ambassador work closely with the Marketing Manager and play an important role promoting The Badger Herald. This is a great opportunity to gain hands-on marketing, outreach, and branding experience while making friends along the way. This position is paid hourly.</p>
<ul>
	<li><a href="http://badgerherald.com/wordpress/media/2014/01/brand-ambassador.pdf">Brand Ambassador Application</a></li>
</ul>

